"""## Mistake & Correction: Avoiding Data Leakage
he Mistake: We initially applied Standardization (Scaling) to the entire dataset before splitting it. This causes Data Leakage, meaning the scaler calculated the mean and standard deviation using the testing data as well. This gives the model an unfair "sneak peek" into the test set, leading to falsely high accuracy scores that will fail in real-world scenarios.

The Correct Approach: > 1. Split the data into Training and Testing sets first.
2. Use fit_transform ONLY on the Training set so the scaler learns its patterns.
3. Use transform ONLY on the Testing set, using the parameters learned from the training data, keeping the test data completely isolated and unseen.
"""

